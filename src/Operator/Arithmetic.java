package Operator;

public class Arithmetic {
    public static void main(String[] args) {
        int a=2;
        int b=4;
        int c=7;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(c/a);
        System.out.println(a*b);
        System.out.println(c%a);
        System.out.println(c*a-b*c/a*c);
        System.out.println();
        double x= 5;
        double y=2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(y/x);
        System.out.println(y*x);
        System.out.println(x%y);

    }
}
