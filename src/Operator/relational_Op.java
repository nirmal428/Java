package Operator;

public class relational_Op {
    public static void main(String[] args) {
        int a=10;
        int d=20;
        int c=10;
        if(a >= d){
            System.out.println(" a is greater than d");
        }
        else{
            System.out.println("a is less than d ");
        }
        if(a == c){
            System.out.println(" a is equal to c");
        }
        else{
            System.out.println("a is notequal to c ");
        }
        if(a != d){
            System.out.println(" a is not equal to d");
        }
        else{
            System.out.println("a is equal to d ");
        }
    }
}
